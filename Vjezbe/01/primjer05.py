'''
                            ---Casting---
'''

S = "2"
A = int(S)         # 2
b = float(S)       # 2.0
c = float("3.14")  # 3.14

print(f"a = {A:4d}, type(a): {type(A)}")
print(f"b = {b:4.2f}, type(b): {type(b)}")
print(f"c = {c:4.2f}, type(c): {type(c)}")

print("----------------------------------")
S1 = str(b)  # '2.0'
print(f"s1 = {S1}, type(s1) : {type(S1)}")
