#                           ---Aritmetičke operacije---

# **  (stepenovanje)
# -   (negacija)
# *   (množenje)
# /   (dijeljenje)
# //  (cjelobrojno dijeljenje)
# %   (ostatak pri dijeljenju)
# +   (sabiranje)
# -   (oduzimanje)

a = 7
b = 4

print(f"{a} + {b} = {a+b}")    # 11
print(f"{a} - {b} = {a-b}")    # 3
print(f"{a} * {b} = {a*b}")    # 28
print(f"{a} / {b} = {a/b}")    # 1.75
print(f"{a} // {b} = {a//b}")  # 1
print(f"{a} % {b} = {a%b}")    # 3
print(f"{a} ** {b} = {a**b}")  # 49 * 49 = 2401
