#                           ---Casting---

s = "2"
a = int(s)         # 2
b = float(s)       # 2.0
c = float("3.14")  # 3.14

print(f"a = {a}, type(a) : {type(a)}")
print(f"b = {b}, type(b) : {type(b)}")
print(f"c = {c}, type(c) : {type(c)}")

print("-------------------------------------")
s1 = str(b)  # '2.0'
print(f"s1 = {s1}, type(s1) : {type(s1)}")
