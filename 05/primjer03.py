# Tuple, tj. n-torka je kolekcija podataka koja je imutabilna.
# To znači da kada kreiramo n-torku više je ne možemo mijenjati,
# tj. ne možemo dodavati nove elemente u torku, ne možemo uklanjati
# postojeće elemente, niti možemo mijenjati vrijednosti postojećih elementa.

torka = (1, "2.3", 4.12)
print("Trojka:", torka)

