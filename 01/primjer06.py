# **  (stepenovanje)
# -   (negacija)
# *   (množenje)
# /   (dijeljenje)
# //  (cjelobrojno dijeljenje)
# %   (ostatak pri dijeljenju)
# +   (sabiranje)
# -   (oduzimanje)

A = 7
B = 4

print(f"{A} + {B} = {A+B}")    # 11
print(f"{A} - {B} = {A-B}")    # 3
print(f"{A} * {B} = {A*B}")    # 28
print(f"{A} / {B} = {A/B}")    # 1.75
print(f"{A} // {B} = {A//B}")  # 1
print(f"{A} % {B} = {A%B}")    # 3
print(f"{A} ** {B} = {A**B}")  # 49 * 49 = 2401
