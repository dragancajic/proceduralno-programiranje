'''
                            ---Osnovni tipovi podataka u Pajtonu---
'''
A = 5               # cijeli  brojevi -> (int)
B = 3.14            # realni brojevi --> (float)
C = 2 + 3j          # kompleksni brojevi (complex)
D = True            # boolean (dvije vrijednosti - True ili False)
E = "Zdravo svima"  # niske (str)

# Pajton ne pravi razliku izmeÄ‘u jednostrukih i dvostrukih navodnika, tako da je
# "Zdravo" ekvivalentno sa 'Zdravo'.

print("A =", A)
print("B =", B)
print("C =", C)
print("D =", D)
print("E =", E)
